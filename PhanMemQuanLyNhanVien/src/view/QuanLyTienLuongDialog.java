    public boolean validateForm(){
        if(txtMaNV.getText().isEmpty()
                || txtHoTen.getText().isEmpty()
                || txtLuongNgay.getText().isEmpty()
                || txtNgayCong.getText().isEmpty()
                || txtPhuCap.getText().isEmpty()
                || txtBaoHiem.getText().isEmpty()){
            return false;
        }else{
            try {
                double luongNgay = Double.parseDouble(txtLuongNgay.getText());
                if(luongNgay<0){
                    return false;
                }
                double ngayCong = Double.parseDouble(txtNgayCong.getText());
                if(ngayCong<0){
                    return false;
                }
                double phuCap = Double.parseDouble(txtPhuCap.getText());
                if(phuCap<0){
                    return false;
                }
                double baoHiem = Double.parseDouble(txtBaoHiem.getText());
                if(baoHiem<0){
                    return false;
                }
            } catch (Exception e) {
                return false;
            }
        }
        return true;
    }
    public Luong getModel(){
        Luong l = new Luong();
        l.setId(0);
        NhanVien nv = new NhanVien(txtMaNV.getText(),txtHoTen.getText());
        l.setNv(nv);
        l.setLuongNgay(Double.parseDouble(txtLuongNgay.getText()));
        l.setNgayCong(Double.parseDouble(txtNgayCong.getText()));
        l.setPhuCap(Double.parseDouble(txtPhuCap.getText()));
        l.setBaoHiem(Double.parseDouble(txtBaoHiem.getText()));
        return l;
    }
    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        if(validateForm()){
            Luong l = getModel();
            if(lDAO.getOneLuongByMaNV(l.getNv().getMaNV())!=null){
                JOptionPane.showMessageDialog(this, "Mã nhân viên này đã tồn tại");
            }else{
                if(lDAO.add(l)>0){
                    JOptionPane.showMessageDialog(this, "Thêm thành công");
                    fillDataTABLE();
                    reset();
                }else{
                    JOptionPane.showMessageDialog(this, "Thêm không thành công");
                }
            }
            
        }else{
            JOptionPane.showMessageDialog(this, "Thông tin nhập vào không hợp lệ");
        }
    }//GEN-LAST:event_btnThemActionPerformed
    
