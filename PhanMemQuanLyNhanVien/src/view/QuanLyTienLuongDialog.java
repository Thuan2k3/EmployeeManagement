    public void fillDataTABLE(){
        DefaultTableModel model = (DefaultTableModel)tbLuong.getModel();
        model.setRowCount(0);
        for (Luong l : lDAO.getAllLuong()) {
            Object rowData[] = new Object[7];
            rowData[0] = l.getNv().getMaNV();
            rowData[1] = l.getNv().getHoTen();
            rowData[2] = l.getLuongNgay();
            rowData[3] = l.getNgayCong();
            rowData[4] = l.getPhuCap();
            rowData[5] = l.getBaoHiem();
            String luongNhanDuoc = String.format("%.2f", l.getLuongNhanDuoc());
            rowData[6] = luongNhanDuoc;
            model.addRow(rowData);
        }
    }

