    public Luong getLuongAtPosition(int pos){
        Luong l = new Luong();
        NhanVien nv = new NhanVien();
        nv.setMaNV(tbLuong.getValueAt(pos, 0).toString());
        nv.setHoTen(tbLuong.getValueAt(pos, 1).toString());
        l.setNv(nv);
        l.setLuongNgay(Double.parseDouble(tbLuong.getValueAt(pos, 2).toString()));
        l.setNgayCong(Double.parseDouble(tbLuong.getValueAt(pos, 3).toString()));
        l.setPhuCap(Double.parseDouble(tbLuong.getValueAt(pos, 4).toString()));
        l.setBaoHiem(Double.parseDouble(tbLuong.getValueAt(pos, 5).toString()));
        return l;
    }
            
            
