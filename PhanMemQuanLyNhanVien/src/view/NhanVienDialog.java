    public void fillDataTable(){
        DefaultTableModel model = (DefaultTableModel)tbNhanVien.getModel();
        model.setRowCount(0); //clear table
        for(NhanVien nv : dao.getAllNhanVien()){
            Object rowData[] = new Object[8];
            rowData[0] = nv.getMaNV();
            rowData[1] = nv.getHoTen();
            rowData[2] = nv.isGioiTinh()?"Nam":"Nữ";
            rowData[3] = date_format.format(nv.getNgaySinh());
            rowData[4] = nv.getDiaChi();
            rowData[5] = nv.getSDT();
            rowData[6] = nv.getEmail();
            rowData[7] = nv.getHinhAnh();
            model.addRow(rowData);
        }
    }
    
