    //Tao 1 nhan vien tu form
    public NhanVien getModel() throws ParseException{
        NhanVien nv = new NhanVien();
        nv.setMaNV(txtMaNV.getText());
        nv.setHoTen(txtHoTen.getText());
        boolean gt = false; //Mac dinh la nu
        if(rdNam.isSelected()){
            gt = true;
        }
        nv.setGioiTinh(gt);
        nv.setNgaySinh(date_format.parse(txtNgaySinh.getText()));
        nv.setDiaChi(txtDiaChi.getText());
        nv.setSDT(txtSDT.getText());
        nv.setEmail(txtEmail.getText());
        if(strHinhAnh == null){
            nv.setHinhAnh("NO IMAGE");
        }else{
            nv.setHinhAnh(strHinhAnh);
        }
    return nv;
    }
    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        if(checkValidateForm()){
            try {
                NhanVien nv = getModel();
                if(dao.add(nv)>0){
                    JOptionPane.showMessageDialog(this, "Thêm thành công");
                    fillDataTable();
                    reset();
                }else{
                    JOptionPane.showMessageDialog(this, "Thêm không thành công");
                }
            } catch (ParseException ex) {
                JOptionPane.showMessageDialog(this, "Dữ liệu không hợp lệ");
            }
        }else{
            JOptionPane.showMessageDialog(this, "Bạn chưa nhập đầy đủ thông tin");
        }
    }//GEN-LAST:event_btnThemActionPerformed
    
    //Dat lai form khi biet nv
    public void setModel(NhanVien nv){
        txtMaNV.setText(nv.getMaNV());
        txtHoTen.setText(nv.getHoTen());
        txtNgaySinh.setText(date_format.format(nv.getNgaySinh()));
        txtDiaChi.setText(nv.getDiaChi());
        if(nv.isGioiTinh()){
            rdNam.setSelected(true);
        }else{
            rdNu.setSelected(true);
        }
        txtSDT.setText(nv.getSDT());
        txtEmail.setText(nv.getEmail());
        //Load hinh anh
        if(nv.getHinhAnh().compareToIgnoreCase("NO IMAGE")==0){
            lblHinhAnh.setText("NO IMAGE");
            lblHinhAnh.setIcon(null);
        }else{
            lblHinhAnh.setText("");
            strHinhAnh = nv.getHinhAnh();
            ImageIcon imgIcon = new ImageIcon(getClass().getResource("/images/"+nv.getHinhAnh()));
            Image img = imgIcon.getImage();
            int width = lblHinhAnh.getWidth();
            int height = lblHinhAnh.getHeight();
            lblHinhAnh.setIcon(new ImageIcon(img.getScaledInstance(width, height, 0)));
        }
    }
    private void tbNhanVienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbNhanVienMouseClicked
        // TODO add your handling code here:
        int id = tbNhanVien.rowAtPoint(evt.getPoint());
        String manv = tbNhanVien.getValueAt(id, 0).toString();
        NhanVien nv = dao.getNhanVienByID(manv);
        setModel(nv);
    }//GEN-LAST:event_tbNhanVienMouseClicked

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        if(txtMaNV.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Bạn chưa nhập mã nhân viên để xóa");
        }else{
            if (dao.delNhanVienByID(txtMaNV.getText())>0) {
                JOptionPane.showMessageDialog(this, "Xóa thành công");
                fillDataTable();
            }else{
                JOptionPane.showMessageDialog(this, "Không tìm thấy nhân viên để xóa");
            }
        }
    }//GEN-LAST:event_btnXoaActionPerformed

